<h2 id="单机版和插件版" tabindex="-1"><a class="header-anchor" href="#单机版和插件版" aria-hidden="true">#</a> 单机版和插件版</h2>
<ul>
<li>单机版</li>
</ul>
<p>为希望将其 Amlogic 设备用作成熟的仿真模拟器系统的人们而设计的，功能较为齐全，它包含更多高级和自定义的选项。个人推荐使用单机版更好。</p>
<ul>
<li>插件版</li>
</ul>
<p>旨在提供那些想要偶尔玩模拟器，而不用更改其 CoreELEC 安装，并且无需关心高级选项和定制。插件版本的最大优势是可以保留 Kodi。</p>
